# What are oops concepts?  Is multiple inheritance supported in java

# OOP (Object-Oriented Programming) is a programming paradigm based on the concept of objects.

# An object is an instance of a class, and a class defines the structure of the object.

# Class: A blueprint for objects.

# Object: An instance of a class.

# Encapsulation: Bundling data and methods together, hiding implementation details.

# Abstraction: Hiding complex implementation details.

# Inheritance: A class can inherit attributes and methods from another class.

# Polymorphism: Methods can have different implementations in subclasses, and objects can be treated as instances of their superclass.

# Multiple Inheritance in Java: Java does not support multiple inheritance of classes but allows multiple inheritance of interfaces.