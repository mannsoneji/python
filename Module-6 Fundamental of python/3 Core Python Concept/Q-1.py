# Write a Python program to demonstrate the creation of variables and different data types

# This is the example of String Data Type
name = "John"
print(f"Name: {name} ---- Type: {type(name)}")

# This is the example of Integer Data Type
age = 25
print(f"Age: {age} ---- Type: {type(age)}")

# This is the example of Float Data Type
height = 5.9
print(f"Height: {height} ---- Type: {type(height)}")


# This is the example of Boolean Data Type
friends = True
print(f"Is Student: {friends} ---- Type: {type(friends)}")

# This is the example of List Data Type
fruits = ["apple", "banana", "cherry"]
print(f"Fruits: {fruits} ---- Type: {type(fruits)}")

# This is the example of Tuple Data Type (immutable)
values = (10.5, 20.5)
print(f"values: {values} ---- Type: {type(values)}")

# This is the example of Dictionary Data Type
person = {"name": "Alice", "age": 30}
print(f"Person: {person} ---- Type: {type(person)}")

# This is the example of Set Data Type (unordered collection)
colors = {"red", "green", "blue"}
print(f"Colors: {colors} ---- Type: {type(colors)}")

#This is the example of None Type
nothing = None
print(f"Nothing: {nothing} ---- Type: {type(nothing)}")
