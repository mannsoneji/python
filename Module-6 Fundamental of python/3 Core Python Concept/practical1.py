# Practical Example 1: How does the Python code structure work?


# Define a variable
age = 25  # Assigning the integer 25 to the variable 'age'

# Conditional Statement (If-Else)
if age >= 18:
    print("You are an adult.")  # This block will execute if age is 18 or older
else:
    print("You are a minor.")  # This block will execute if age is less than 18

# Print a simple message
print("This program has ended.")





