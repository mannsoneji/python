# Write a Python program to create a tuple with different data types.

t1 = (42, "Hello", 3.14, True, (1, 2), [1, 2, 3])

print("Data type of first element (42):", type(t1[0])) 

print("Data type of second element ('Hello'):", type(t1[1]))  

print("Data type of third element (3.14):", type(t1[2]))

print("Data type of fourth element (True):", type(t1[3])) 

print("Data type of fifth element ((1, 2)):", type(t1[4])) 

print("Data type of sixth element ([1, 2, 3]):", type(t1[5]))

