# What is List? How will you reverse a list?

# A list is a built-in data structure that allows you to store a collection of items. 

# These items can be of different types, including numbers, strings, boolean etc. 

# Lists are ordered, changeable, and allow duplicate elements. 

# Lists are defined using square brackets []

l1 = [1,2,3,4,5]
l1.reverse()
print(l1)

l2 = [1,2,3,4,5]
reversed_list = list(reversed(l2))
print(reversed_list)